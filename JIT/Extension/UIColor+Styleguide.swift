import Foundation
import UIKit

extension UIColor {
    
    static var primaryYellow: UIColor {
        return UIColor(red: 255 / 255.0, green: 144 / 255.0, blue: 0 / 255.0, alpha: 1.0)
    }
    
    static var darkWhite: UIColor {
        return UIColor(red: 245 / 255.0, green: 245 / 255.0, blue: 245 / 255.0, alpha: 1.0)
    }
}
